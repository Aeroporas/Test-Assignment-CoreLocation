        if let launchOptions = launchOptions, launchOptions.contains(where: {$0.key == .location}) {
            if let rootVC = window?.rootViewController as? MainViewController {
                rootVC.updateBarButtonState(trackingIsInProgress: true)
                print("App launched by location update")
            }
        }
        
        // Override point for customization after application launch.
        return true
    }

